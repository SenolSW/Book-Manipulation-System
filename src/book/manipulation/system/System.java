//Insert    
    private void btnInsertActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInsertActionPerformed
        if (txtISBNno.getText().trim().isEmpty() || txtTitle.getText().trim().isEmpty() || txtAuthor1.getText().trim().isEmpty()
                && txtAuthor2.getText().trim().isEmpty() || txtPrice.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(null, "You cannot leave this field/s empty", "Insert error", JOptionPane.ERROR_MESSAGE);

        } else {
            String ISBN = txtISBNno.getText();
            String Title = txtTitle.getText();
            String Author1 = txtAuthor1.getText();
            String Author2 = txtAuthor2.getText();
            String Author = Author1 + ";" + Author2;
            int Price = Integer.parseInt(txtPrice.getText());
            if (!C.DuplicateSearch(ISBN)) {

                if (txtAuthor2.getText().trim().isEmpty()) {
                    C.Insert(ISBN, Title, Author, Price);
                    jTextArea.setText("ISBN No: " + ISBN + " Inserted!");
                    //jTextArea.setText(ISBN+","+Title+","+Author+","+Price);
                } else {
                    C.Insert(ISBN, Title, Author, Price);
                    jTextArea.setText("ISBN No: " + ISBN + " Inserted!");
                    //jTextArea.setText(ISBN+","+Title+","+Author1+";"+Author2+","+Price);
                }

            } else {
                jTextArea.setText(ISBN + " Already Exsits");
            }
        }
    }//GEN-LAST:event_btnInsertActionPerformed

