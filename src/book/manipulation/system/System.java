//LoadData    
    private void loadData() {
        try {
            x = new Scanner(new File("BookInfo.txt"));
            x.useDelimiter("[,\n]");
            while (x.hasNext()) {
                C.ISBNnNo[C.elements] = x.next();
                C.Title[C.elements] = x.next();
                C.Author[C.elements] = x.next();
                C.Price[C.elements] = Integer.parseInt(x.next());
                C.elements++;
            }
            x.close();
        } catch (IOException ex) {
            Logger.getLogger(Class.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

//ReadData
    private void ReadData() {
        try {
            if (TextFile.exists() && TextFile.length() == 0) {
                JOptionPane.showMessageDialog(null, "The text file exists and is empty!");
                loadData();
            } else if (TextFile.exists() && TextFile.length() != 0) {
                JOptionPane.showMessageDialog(null, "The text file exists and is not empty!");
                loadData();
            } else {
                JOptionPane.showMessageDialog(null, "The text file does not exists!");
                TextFile.createNewFile();
                loadData();
            }
        } catch (IOException ex) {
            Logger.getLogger(Class.class.getName()).log(Level.SEVERE, null, ex);
        }

    }

//InsertFile    
    public void InsertFile() {
        try {
            String result = "";
            for (int x = 0; x < C.elements; x++) {
                result += C.ISBNnNo[x] + ",";
                result += C.Title[x] + ",";
                result += C.Author[x] + ",";
                result += C.Price[x] + "\n";
            }
            File newFile = new File("Temp.txt");
            FileWriter TempFile = new FileWriter(newFile, true);
            BufferedWriter writer = new BufferedWriter(TempFile);
            writer.append(result);
            writer.close();
            TextFile.delete();
            File dump = new File("BookInfo.txt");
            newFile.renameTo(dump);
        } catch (IOException ex) {
            Logger.getLogger(Class.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

